function [A,b,dim] = splinesCubico (x,y,n)
	A = zeros(4*(n-1),4*(n-1));
	b = zeros(4*(n-1),1);
    for i = 1:(n-1)
		A(2*i-1,4*i-3) = x(i)^3;
		A(2*i-1,4*i-2) = x(i)^2;
		A(2*i-1,4*i-1) = x(i);
		A(2*i-1,4*i) = 1;
		b(2*i-1) = y(i);
		A(2*i,4*i-3) = x(i+1)^3;
		A(2*i,4*i-2) = x(i+1)^2;
		A(2*i,4*i-1) = x(i+1);
		A(2*i,4*i) = 1;
		b(2*i) = y(i+1);
    end
    for i = 1:(n-2)
		A(2*n-2+i,4*i-3) = 3*x(i+1)^2;
		A(2*n-2+i,4*i-2) = 2*x(i+1);
		A(2*n-2+i,4*i-1) = 1;
		A(2*n-2+i,4*i+1) = -3*x(i+1)^2;
		A(2*n-2+i,4*i+2) = -2*x(i+1);
		A(2*n-2+i,4*i+3) = -1;
		b(2*n-2+i) = 0;
    end
    for i = 1:(n-2)
		A(3*n-4+i,4*i-3) = 6*x(i+1);
		A(3*n-4+i,4*i-2) = 2;
		A(3*n-4+i,4*i+1) = -6*x(i+1);
		A(3*n-4+i,4*i+2) = -2;
		b(3*n-4+i) = 0;
    end
	A(4*n-5,1) = 6*x(1);
	A(4*n-5,2) = 2;
	A(4*n-4,4*n-7) = 6*x(n);
	A(4*n-4,4*n-6) = 2;
	b(4*n-5) = 0;
	b(4*n-4) = 0;
    dim = 4*(n-1);
end